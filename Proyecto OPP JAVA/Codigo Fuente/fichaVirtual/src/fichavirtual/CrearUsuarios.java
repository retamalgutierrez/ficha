    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String clave1 = "";
        for (int i = 0; i < txtPassInicial.getPassword().length ; i++) {
            clave1 += (txtPassInicial.getPassword())[i];
        }

        String clave2 = "";
        for (int i = 0; i < txtPassFinal.getPassword().length ; i++) {
            clave2 += (txtPassFinal.getPassword())[i];
        }
        
        if(!"".equals(txtNombres.getText()) &&
           !"".equals(txtApellidos.getText()) &&
           !"".equals(txtUsuario.getText()) &&
           !"".equals(clave1) &&
           !"".equals(clave2))
        {
            if(clave1.equals(clave2))
            {
                int reply = JOptionPane.showConfirmDialog(
                null, "¿Esta seguro que desea continuar?",
                "Atención!", 
                JOptionPane.YES_NO_OPTION);
                if (reply == JOptionPane.YES_OPTION) {
                    Usuarios user = new Usuarios(txtNombres.getText(),txtApellidos.getText(),txtUsuario.getText(),clave1,comboTipo.getSelectedItem().toString());
                    if(user.registrarUsuario(imagen,txtFoto.getText()))
                    {
                        JOptionPane.showMessageDialog(null, "Usuario Ingresado");
                        JDialog parentDialog=(JDialog)SwingUtilities.getWindowAncestor(this); 
                        parentDialog.dispose();

                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
                    }
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Claves Incorrectas");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Error, no se permiten campos nulos");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

