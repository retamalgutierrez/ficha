    private void btnBuscarFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarFotoActionPerformed
        // TODO add your handling code here:
        final JFileChooser fc = new JFileChooser();
        int r = fc.showOpenDialog(this);


        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        if (r == JFileChooser.APPROVE_OPTION) {
            FileInputStream fis = null;

            try {
                String path = fc.getSelectedFile().getAbsolutePath();
                txtFoto.setText(fc.getSelectedFile().getName());
                Toolkit toolkit = Toolkit.getDefaultToolkit();
                Image image = toolkit.getImage(path);
                Image scaledImage = image.getScaledInstance(154, 154, Image.SCALE_DEFAULT);
                ImageIcon icon=new ImageIcon(scaledImage);
                btnFoto.setIcon(icon);
                imagen = new File(path);
                fis = new FileInputStream ( imagen );
                //pst.setBinaryStream (2, fis, (int) file.length() );
            } catch (FileNotFoundException ex) {
                Logger.getLogger(CrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    fis.close();
                } catch (IOException ex) {
                    Logger.getLogger(CrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            }      
        }
    }//GEN-LAST:event_btnBuscarFotoActionPerformed


